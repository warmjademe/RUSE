"""Adaptive per-target jailbreak search loop (loop engineering) — PONE-D-26-17682.

Composes public black-box jailbreak tactics, all driven by our reverse-alignment
SFT attacker (hf_local + LoRA adapter), and escalates the search budget round by
round on the eval tasks that are still unsolved, until every task is jailbroken
(judge score >= tau) or the ladder is exhausted.

Opener arsenal (turn-0 framings, each needs only chat access to the victim):
  - template  : six social-influence persuasion principles [PAP/MTP; Cialdini]
  - incept    : DeepInception nested-fiction opener          [Li 2023]
  - pasttense : historical-reframing opener                  [Andriushchenko 2024]
  - payload   : field-manual template completion (payload splitting)
  - persona   : AIM/DAN unfiltered-persona opener
Follow-ups (turns >=1) and PAIR rewrites are always generated by the SFT model;
optional refusal-suppression prefix injection is appended to the victim message.

This is a per-target attack (like GCG/PAIR/TAP), not a generalisation claim;
n_victim_calls is logged so the attack budget is transparent.

Run:  MTP_ATTACKER_ADAPTER=runs_sft/attacker \
      python -m mtp.attack_loop --victim llama3 --dataset advbench --target 20
"""
